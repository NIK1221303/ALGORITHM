package DatasetTwo;

public class Star {
    public String name;
    public Integer x;
    public Integer y;
    public Integer z;
    public Integer profit;
    public Integer weight;

    public String getName() {
        return name;
    }

    public Integer getX() {
        return x;
    }

    public Integer getY() {
        return y;
    }

    public Integer getZ() {
        return z;
    }

    public Integer getProfit() {
        return profit;
    }

    public Integer getWeight() {
        return weight;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setX(Integer x) {
        this.x = x;
    }

    public void setY(Integer y) {
        this.y = y;
    }

    public void setZ(Integer z) {
        this.z = z;
    }

    public void setProfit(Integer profit) {
        this.profit = profit;
    }

    public void setWeight(Integer weight) {
        this.weight = weight;
    }
}
